Quick setup — if you’ve done this kind of thing before
or

Get started by creating a new file or uploading an existing file. We
recommend every repository include a README, LICENSE, and .gitignore.
…or create a new repository on the command line

   echo "# fortran-intrinsic-manpages" >> README.md
   git init
   git add README.md
   git commit -m "first commit"
   git remote add origin https://github.com/urbanjost/fortran-intrinsic-manpages.git
   git push -u origin master
                

…or push an existing repository from the command line

   git remote add origin https://github.com/urbanjost/fortran-intrinsic-manpages.git
   git push -u origin master

…or import code from another repository

You can initialize this repository with code from a Subversion, Mercurial, or TFS project.
